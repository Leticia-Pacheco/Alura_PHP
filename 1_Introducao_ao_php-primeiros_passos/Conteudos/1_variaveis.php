<?php

// O PHP usa a tipagem dinâmica, ou seja, não precisamos definir o tipo da variável porque ele faz isso por si só.
// Podemos declarar a variável na hora de executá-la.
// Para declarar um texto (String), devemos colocá-lo dentro de aspas.
$nome = "Letícia Pacheco";

// Para declararmos um número (int, double ou float) apenas colocamos ele, sem aspas nem nada.
$idade = 17;


// Mesmo com o valor definido, é possível alterá-la depois, pois é uma VARIÁVEL.
$idade = 18;

// o echo serve para exibir o comando na tela.
echo $nome;

// Quando alteramos a variável, o echo sempre chamará o último valor definido.